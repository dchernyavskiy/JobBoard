import { Injectable } from '@angular/core';
import { Client, GetJobsQuery, JobLookupDto, JobFilter, Pagging, JobSort  } from '../api/api';
@Injectable({
  providedIn: 'root'
})
export class JobService {

  constructor (public client: Client) { }

  getAll(body: GetJobsQuery) : JobLookupDto[] {
    let jobs : JobLookupDto[];
    this.client.getAllPOST('1', body).subscribe(result =>{
      console.log(result);
      jobs = result.jobs;  
    });
    console.log("before return: " + jobs);
    return jobs;
  }
}
























